class arithmeticoperator
{
public static void main(String args[])
{
int n1=100;
int n2=10;
System.out.println("addition operator "+n1+"+"+n2+"="+(n1+n2));
System.out.println("subtraction operator "+n1+"-"+n2+"="+(n1-n2));
System.out.println("mutliplication operator "+n1+"*"+n2+"="+(n1*n2));
System.out.println("division operator "+n1+"/"+n2+"="+(n1/n2));
System.out.println("remainder or modulus operator "+n1+"%"+n2+"="+(n1%n2));
}
}


