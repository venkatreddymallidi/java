// "\n" and "\t"
class Newline
{
public static void main(String args[])
{
System.out.println("numbers in separate line:");
for(int i=1;i<5;i++)
{
System.out.println(i);  //println=printinline
}
System.out.println("numbers with tab space:");
for(int j=1;j<5;j++)
{
System.out.print(j+" "); //print=print in same line and empty space quotes for tabspace
}
}
}
